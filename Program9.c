//////////////////////////////////////////////////////////////////////
//Algorithm
/////////////////////////////////////////////////////////////////////

/*
    START 
       Accept Number from User as No 
       Divide that No by 5 and Check the Value of Remainder 
       If the Value is 0 
           Then Display as No is Divisible by 5 
        Otherwise 
           Display as No is not Divisible by 5 
    END 
*/

////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//
// Fuction Name : DivisibleByFive 
// Description : To Check Whether the Input is Divisible by five or not 
// Input : Integer 
// Output : Integer 
// Author : Arvind Motiram Kale 
// Date : !2/10/2022
//
///////////////////////////////////////////////////////////////////////

int DivisibleByFive(int iNo)
{
    int iAns = 0 ;
    iAns = iNo % 5 ;
    
    if(iAns != 0 ) //  == : Comparison Operator 
    {
        return 0 ;
    }
    else
    {
        return 1 ;
    }
}

//////////////////////////////////////////////////////////////////////////
//Entry Point of the Application
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0 ;
    int iRet = 0 ;

    printf("Enter Number : \n");
    scanf("%d",&iValue);
    
    iRet = DivisibleByFive(iValue);

    if(iRet != 0 )
    {
        printf("%d is  Divisible by 5\n",iValue);
    }
    else 
    {
        printf("%d is not Divisible by 5\n",iValue);
    }

    return 0;
}

